"""Generate the results scoreboard from saved eval reports.

Reads every JSON report in code/evaluation/reports/ (each produced by
`evaluation.main --json ... --tag ...`) and writes a single markdown comparison
table to code/docs/results.md. Pure aggregation — no transcription, no API, no
model runs — so the table can never drift from the scorer's output.

Usage (from anywhere):
  .venv/bin/python code/evaluation/scoreboard.py
"""

from __future__ import annotations

import glob
import json
import os
import sys

_CODE_ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
if _CODE_ROOT not in sys.path:
    sys.path.insert(0, _CODE_ROOT)

import schema  # noqa: E402

REPORTS_DIR = os.path.join(_CODE_ROOT, "evaluation", "reports")
OUT = os.path.join(_CODE_ROOT, "docs", "results.md")

# claim_status (headline) first, then the rest in schema order.
COLUMNS = [schema.HEADLINE_FIELD] + [f for f in schema.SCORED_FIELDS if f != schema.HEADLINE_FIELD]


def _metric(field_result: dict):
    """Return the headline metric for a field: accuracy (scalar) or micro-F1 (set)."""
    if field_result["kind"] == "scalar":
        return field_result["accuracy"]
    return field_result["micro_f1"]


def _pct(x):
    return "-" if x is None else f"{x * 100:.0f}"


def build_table(reports: list) -> str:
    header = "| Config | " + " | ".join(
        (f"**{c}**" if c == schema.HEADLINE_FIELD else c) for c in COLUMNS
    ) + " | n |"
    sep = "|" + "---|" * (len(COLUMNS) + 2)
    lines = [header, sep]
    for rep in reports:
        tag = rep.get("tag") or rep.get("_file", "?")
        n = rep.get("summary", {}).get("n_pairs", "")
        cells = []
        for c in COLUMNS:
            fr = rep.get("fields", {}).get(c)
            cells.append(_pct(_metric(fr)) if fr else "-")
        lines.append(f"| {tag} | " + " | ".join(cells) + f" | {n} |")
    return "\n".join(lines)


def main() -> int:
    paths = sorted(glob.glob(os.path.join(REPORTS_DIR, "*.json")))
    reports = []
    for p in paths:
        with open(p, encoding="utf-8") as f:
            rep = json.load(f)
        rep["_file"] = os.path.splitext(os.path.basename(p))[0]
        reports.append(rep)

    body = (
        "<!-- GENERATED by evaluation/scoreboard.py from evaluation/reports/*.json — do not edit by hand. -->\n\n"
        "# Results scoreboard (sample, n=20)\n\n"
        "Headline = `claim_status` accuracy. Set fields (`risk_flags`, `supporting_image_ids`) "
        "are micro-F1; the rest are accuracy. All values are percentages. "
        "Narrative + decisions: `experiment_log.md`.\n\n"
    )
    body += (build_table(reports) if reports else "_(no reports yet)_") + "\n"

    with open(OUT, "w", encoding="utf-8") as f:
        f.write(body)
    print(f"Wrote {OUT} ({len(reports)} configs)")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
